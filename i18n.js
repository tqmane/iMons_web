  const langFonts = {
    ja: '"Inter", -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif',
    en: '"Inter", -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif',
    zh: '"Noto Sans SC", "Inter", -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif',
    ko: '"Noto Sans KR", "Inter", -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif'
  };

  function applyLang(lang) {
    const dict = translations[lang];
    if (!dict) return;

    document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
    document.body.style.fontFamily = langFonts[lang] || langFonts.en;

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      if (dict[key] != null) {
        el.innerHTML = dict[key];
      }
    });

    document.querySelectorAll(".lang-btn").forEach(function (btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
    });

    try { localStorage.setItem("imonos-lang", lang); } catch (e) { /* noop */ }
  }

  // Init
  var saved = null;
  try { saved = localStorage.getItem("imonos-lang"); } catch (e) { /* noop */ }
  var initial = saved && translations[saved] ? saved : "ja";
  applyLang(initial);

  // Bind buttons
  document.querySelectorAll(".lang-btn").forEach(function (btn) {
    btn.addEventListener("click", function () {
      applyLang(btn.getAttribute("data-lang"));
    });
  });
